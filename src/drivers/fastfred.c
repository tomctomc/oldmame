/***************************************************************************

Fast Freddie memory map (preliminary)

This game runs on a modified Galaxian board. Three color planes, 4 banks
of characters and double the number of sprites were added.

Main CPU

0000-7fff ROM
c000-c7ff RAM
d000-d3ff Video RAM
d400-d7ff Mirrored Video RAM
d800-dbff Color RAM

I/O read:

c800-cfff Custom I/O (Protection)
e000 Input Port 1
e800 Input Port 2
f000 Dip Switches
f800 Watchdog Reset

I/O write:

c800-cfff Custom I/O write? A couple of these locations are
          written to during start up
e000 ?
f001 NMI Enable
f002-f003 Palette Select (?)
f003-f004 Character Bank Select
f006-f007 ?
f116 Screen Flip X
f117 Screen Flip Y
f800 Sound Command Write


Sound CPU

0000-1fff ROM
2000-23ff RAM

I/O read:

3000 Sound Command Read

I/O write:

3000 NMI Enable
4000 ? (Set to 0 at the begining)
5000 AY8910 #1 Control Port
5001 AY8910 #1 Write Port
6000 AY8910 #2 Control Port
6001 AY8910 #2 Write Port

***************************************************************************/

