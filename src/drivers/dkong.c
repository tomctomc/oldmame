void dkong3_2a03_reset_w(int offset,int data)
{
	if ((data & 1) == 0)
	{
		/* suspend execution */
		cpu_halt(1,0);
		cpu_halt(2,0);
	}
	else
	{
		/* reset and resume execution */
		cpu_reset(1);
		cpu_halt(1,1);
		cpu_reset(2);
		cpu_halt(2,1);
	}
}

