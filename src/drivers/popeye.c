static int dswbit;

static void popeye_portB_w(int offset,int data)
{
	dswbit = data >> 1;
}

static int popeye_portA_r(int offset)
{
	int res;


	res = input_port_3_r(offset);
	res |= (input_port_4_r(offset) << (7-dswbit)) & 0x80;

	return res;
}

